CREATE DATABASE JOINS;
USE JOINS;

-- Create Table Statements
CREATE TABLE DEPARTMENT(
    DEPT_ID VARCHAR (60) PRIMARY KEY,
    DEPT_NAME VARCHAR (60),
    BUDGET INT ,
    BUILDING VARCHAR (60)
);

CREATE TABLE TEACHER (
    TEACHER_ID VARCHAR (60) PRIMARY KEY,
    TEACHER_NAME VARCHAR(60),
    DEPT_ID VARCHAR(60),
    FOREIGN KEY(DEPT_ID) REFERENCES DEPARTMENT(DEPT_ID),
    SALARY INT,
    HIREDATE DATE
);

CREATE TABLE SUBJECT (
    SUB_ID VARCHAR(60) PRIMARY KEY,
    SUB_NAME VARCHAR(60),
    DEPT_ID VARCHAR(60),
    FOREIGN KEY(DEPT_ID) REFERENCES DEPARTMENT(DEPT_ID),
    TEACHER_ID VARCHAR(60),  
    FOREIGN KEY(TEACHER_ID) REFERENCES TEACHER(TEACHER_ID),
    CREDITS INT
);

CREATE TABLE STUDENT(
    STUD_ID VARCHAR(60) PRIMARY KEY,
    STUD_NAME VARCHAR(60),
    DEPT_ID VARCHAR(60), 
    FOREIGN KEY(DEPT_ID) REFERENCES DEPARTMENT(DEPT_ID),
    GPA DECIMAL(3,2),  -- Adjusted to explicitly allow decimal places like 3.85
    ENR_YEAR INT 
);

CREATE TABLE CLASSROOM (
    CLASS_ID VARCHAR (60) PRIMARY KEY,
    ROOM_NAME VARCHAR (60),
    SUB_ID VARCHAR (60),
    DEPT_ID VARCHAR(60),
    FOREIGN KEY(DEPT_ID) REFERENCES DEPARTMENT(DEPT_ID),
    C_CAPCT INT,        -- Kept your original column name
    HAS_PROJECTOR BOOLEAN -- Kept your original column name
);

