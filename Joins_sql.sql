#INNER JOIN
select T1.teacher_id,
T1.teacher_name,
D1.DEPT_NAME
FROM 
DEPARTMENT D1 INNER JOIN TEACHER T1
ON 
D1.DEPT_ID=T1.DEPT_ID;

#LEFT JOIN
SELECT C1.CLASS_ID,
C1.ROOM_NAME,
S1.SUB_NAME
FROM 
CLASSROOM C1 LEFT JOIN SUBJECT S1
ON
C1.SUB_ID=S1.SUB_ID;

#RIGHT JOIN
SELECT C1.CLASS_ID,
C1.ROOM_NAME,
S1.SUB_NAME
FROM 
SUBJECT S1 RIGHT JOIN CLASSROOM C1
ON 
C1.SUB_ID=S1.SUB_ID;

